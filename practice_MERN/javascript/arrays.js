//arrays

const array1 = [1, 2, 3, 4, 5];
console.log(array1); // prints the array [1, 2, 3, 4, 5]
console.log(array1.length); // prints the length of the array, which is 5
array1[0] = 10; // changes the first element of the array to 10
console.log(array1); // prints the updated array [10, 2, 3, 4, 5]
//we can not add new or delete elements in the array, it is fixed size, 
// but even it is constant, we can change the elements

